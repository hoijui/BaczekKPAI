    @Action
    public void askForStatusTxt() {
        JFileChooser fc = new JFileChooser();
        int ret = fc.showOpenDialog(drawingPanel1);
        if (ret == JFileChooser.APPROVE_OPTION) {
            readStatusFile(fc.getSelectedFile());
        }
    }

    public void fillDrawingPanel(StatusReader sr) {
        drawingPanel1.setFrameNum(sr.getFrameNum());
        drawingPanel1.setMaph(sr.getMapHeight());
        drawingPanel1.setMapw(sr.getMapWidth());
        drawingPanel1.setFriendlies(sr.getFriendlies());
        drawingPanel1.setEnemies(sr.getEnemies());
        drawingPanel1.setGeovents(sr.getGeovents());
    }

