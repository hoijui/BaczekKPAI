    private void readStatusFile(File file) {
        BufferedReader r = null;
        try {
            setFilename(file);
            r = new BufferedReader(new InputStreamReader(new DataInputStream(new FileInputStream(file))));
            StatusReader sr = new StatusReader(r);
            sr.parse();
            fillDrawingPanel(sr);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(BaczekKPAIVisView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(BaczekKPAIVisView.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                r.close();
            } catch (IOException ex) {
                Logger.getLogger(BaczekKPAIVisView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void setFilename(File selectedFile) {
        try {
            setFilename(selectedFile.getCanonicalPath());
        } catch (IOException ex) {
            Logger.getLogger(BaczekKPAIVisView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    @Action
    public void openStatusFileFromTextBox() {
        File file = new File(jTextField1.getText());
        if (file.canRead()) {
            readStatusFile(file);
        }
    }
